function getLang() {
  const params = new URLSearchParams(window.location.search);
  const urlLang = params.get('lang');
  if (urlLang && translations[urlLang]) return urlLang;
  try {
    const stored = localStorage.getItem('qc-lang');
    if (stored && translations[stored]) return stored;
  } catch (e) {}
  return 'en';
}

let currentLang = getLang();

function t(key) {
  return translations[currentLang]?.[key] || translations['en']?.[key] || key;
}

function updateLinks() {
  document.querySelectorAll('a[href]').forEach(function(a) {
    var href = a.getAttribute('href');
    if (!href || href.startsWith('#') || href.startsWith('http') || href.startsWith('mailto') || href.startsWith('tel')) return;
    href = href.replace(/\?lang=(en|sk)/, '');
    var hash = '';
    var hashIdx = href.indexOf('#');
    if (hashIdx !== -1) {
      hash = href.substring(hashIdx);
      href = href.substring(0, hashIdx);
    }
    a.setAttribute('href', href + '?lang=' + currentLang + hash);
  });
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = text;
    } else if (el.tagName === 'OPTION') {
      el.textContent = text;
    } else {
      el.innerHTML = text.replace(/\n/g, '<br>');
    }
  });

  document.querySelectorAll('.lang-toggle').forEach(btn => {
    btn.textContent = currentLang === 'en' ? 'SK' : 'EN';
  });

  document.documentElement.lang = currentLang;

  updateLinks();

  try { document.dispatchEvent(new CustomEvent('langchange')); } catch (e) {}
}

function toggleLang() {
  currentLang = currentLang === 'en' ? 'sk' : 'en';
  try { localStorage.setItem('qc-lang', currentLang); } catch (e) {}
  try { applyTranslations(); } catch (e) {}
}

window.toggleLang = toggleLang;

function init() {
  try { applyTranslations(); } catch (e) {}
  document.querySelectorAll('.lang-toggle').forEach(function(btn) {
    btn.onclick = function() {
      toggleLang();
      return false;
    };
  });
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', init);
} else {
  init();
}
